string reverseStr(string& str)
{
    int n = str.length();
 
    // Swap character starting from two
    // corners
    for (int i = 0; i < n / 2; i++)
        swap(str[i], str[n - i - 1]);

    return str;
}
string operation1(string str){
    string original=str;
    string res=reverseStr(str);
    return original.append(res);
}
