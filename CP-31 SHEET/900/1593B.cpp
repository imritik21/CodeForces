void solve()
{
    // Make it Divisible by 25
    //basicalyy you need to find the num that is %25
    // ___7__5__
    //    i |j |
    //     dist b/w 7 &5= j-i-1 and dist b/w 5 and las = n-1-j
    string s;
    cin>>s;
    int n=s.size();
    int ans=n;
    for(int i=0;i<n;i++){
        for(int j=i+1;j<n;j++){
            int num = (s[i]-'0')*10+(s[j]-'0');
            if(num%25==0){
                ans=min(ans,(j-i-1)+(n-1-j));
            }
        }
    }
    cout<<ans<<endl;
}
